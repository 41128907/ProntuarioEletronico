    public void preencherTabela(String sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Nome", "Tipo"};
        conecta.connection();
        conecta.execulteSQL(sql);
        try {
             conecta.rs.first();
             do{
                 dados.add(new Object[]{conecta.rs.getInt("cod_usuario"), conecta.rs.getString("nome_usuario"), conecta.rs.getString("tipo_usuario")});
             }while(conecta.rs.next());
        } catch (SQLException ex) {
            jButtonExcluir.setEnabled(false);
            jButtonAlterar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jTextFieldCodUser.setText("");
            jTextFieldUsuarioUser.setText("");
            jPasswordFieldSenhaUser.setText("");
            jPasswordFieldConfirmarSenhaUser.setText("");
            jComboBoxTipoUser.setSelectedIndex(0);
            JOptionPane.showMessageDialog(rootPane, "Tente novamente! Erro ao preencher tabela!") ;
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableUsuario.setModel(modelo);
        jTableUsuario.getColumnModel().getColumn(0).setPreferredWidth(30);
        jTableUsuario.getColumnModel().getColumn(0).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTableUsuario.getColumnModel().getColumn(1).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(2).setPreferredWidth(120);
        jTableUsuario.getColumnModel().getColumn(2).setResizable(false);
        jTableUsuario.getTableHeader().setReorderingAllowed(false);
        jTableUsuario.setAutoResizeMode(jTableUsuario.AUTO_RESIZE_OFF);
        jTableUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conecta.disconnection();
    }
    
