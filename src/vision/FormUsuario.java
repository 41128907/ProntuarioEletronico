    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        
        if (jTextFieldUsuarioUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Usuário para continuar!");
            jTextFieldUsuarioUser.requestFocus();
        }else if(jPasswordFieldSenhaUser.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o campo Senha para continuar!");
            jPasswordFieldSenhaUser.requestFocus();
        }else if(jPasswordFieldConfirmarSenhaUser.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o campo Confirmar Senha para continuar!");
            jPasswordFieldConfirmarSenhaUser.requestFocus();
        }else if(jPasswordFieldSenhaUser.getText().equals(jPasswordFieldConfirmarSenhaUser.getText())){
            if (flag == 1) {
                model.setUserName(jTextFieldUsuarioUser.getText());
                model.setUserType((String)jComboBoxTipoUser.getSelectedItem());
                model.setUserPass((jPasswordFieldSenhaUser.getText()));
                dao.salvar(model);
                jTextFieldUsuarioUser.setText("");
                jPasswordFieldSenhaUser.setText("");
                jPasswordFieldConfirmarSenhaUser.setText("");
                jTextFieldCodUser.setEnabled(false);
                jTextFieldUsuarioUser.setEnabled(false);
                jPasswordFieldSenhaUser.setEnabled(false);
                jPasswordFieldConfirmarSenhaUser.setEnabled(false);
                jComboBoxTipoUser.setEnabled(false);
                jButtonSalvar.setEnabled(false);
                jButtonNovo.setEnabled(true);
                jButtonCancelar.setEnabled(false);
                jButtonAlterar.setEnabled(false);
                jButtonExcluir.setEnabled(false);
                jTextFieldCodUser.setText("");
                jTextFieldPesquisarUser.setEnabled(true );
                jButtonBuscarUser.setEnabled(true);
                preencherTabela("select * from usuarios order by nome_usuario");
            } else{
                model.setUserCode(Integer.parseInt(jTextFieldCodUser.getText()));
                model.setUserName(jTextFieldUsuarioUser.getText());
                model.setUserType((String)jComboBoxTipoUser.getSelectedItem());
                model.setUserPass((jPasswordFieldSenhaUser.getText()));
                dao.Editar(model);
                jTextFieldUsuarioUser.setText("");
                jPasswordFieldSenhaUser.setText("");
                jPasswordFieldConfirmarSenhaUser.setText("");
                jTextFieldCodUser.setEnabled(false);
                jTextFieldUsuarioUser.setEnabled(false);
                jPasswordFieldSenhaUser.setEnabled(false);
                jPasswordFieldConfirmarSenhaUser.setEnabled(false);
                jComboBoxTipoUser.setEnabled(false);
                jButtonSalvar.setEnabled(false);
                jButtonNovo.setEnabled(true);
                jButtonCancelar.setEnabled(false);
                jTextFieldCodUser.setText("");
                jButtonAlterar.setEnabled(false);
                jButtonExcluir.setEnabled(false);
                jTextFieldPesquisarUser.setEnabled(true );
                jButtonBuscarUser.setEnabled(true);
                preencherTabela("select * from usuarios order by nome_usuario");
            }
        }else{
            JOptionPane.showMessageDialog(null, "As senhas não correspondem!");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jTableUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsuarioMouseClicked
        String nome_usuario = ""+jTableUsuario.getValueAt(jTableUsuario.getSelectedRow(), 1);
        conecta.connection();
        conecta.execulteSQL("select * from usuarios where nome_usuario='" + nome_usuario + "'");
         try {
            conecta.rs.first();
            jTextFieldCodUser.setText(String.valueOf(conecta.rs.getInt("cod_usuario")));
            jTextFieldUsuarioUser.setText(conecta.rs.getString("nome_usuario"));
            jComboBoxTipoUser.setSelectedItem(conecta.rs.getString("tipo_usuario"));
            jPasswordFieldSenhaUser.setText(conecta.rs.getString("senha_usuario"));
            jPasswordFieldConfirmarSenhaUser.setText(conecta.rs.getString("senha_usuario"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar dados!");
        }
        conecta.disconnection();
        jButtonAlterar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonNovo.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(true);
        jTextFieldCodUser.setEnabled(false);
        jTextFieldUsuarioUser.setEnabled(false);
        jPasswordFieldSenhaUser.setEnabled(false);
        jPasswordFieldConfirmarSenhaUser.setEnabled(false);
        jComboBoxTipoUser.setEnabled(false);
    }//GEN-LAST:event_jTableUsuarioMouseClicked

