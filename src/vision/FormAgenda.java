    public void preencherMedicos(){
        conecta.connection();
        conecta.execulteSQL("select * from medicos order by nome_medico");
        try {
             conecta.rs.first();
             jComboBoxMedico.removeAllItems();
             do{
                 jComboBoxMedico.addItem(conecta.rs.getString("nome_medico"));
                 
             }while(conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Tente novamente uma nova busca!" + ex) ;
        }
        
        conecta.disconnection();
    }
    
